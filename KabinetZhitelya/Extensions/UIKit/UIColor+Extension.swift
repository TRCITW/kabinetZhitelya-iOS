import UIKit

extension UIColor {
    
    func setupCustomBlue() -> UIColor? {
        return UIColor(red: 3/255, green: 103/255, blue: 204/255, alpha: 1.0)
    }
    
    func setupCustomDarkGray() -> UIColor? {
        return UIColor(red: 82/255, green: 82/255, blue: 82/255, alpha: 1.0)
    }
    
    func setupCustomLightGray() -> UIColor? {
        return UIColor(red: 147/255, green: 147/255, blue: 147/255, alpha: 1.0)
    }
    
    func setupBackgroundGray() -> UIColor? {
        return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1.0)
    }
    
    func setupPaleBlue() -> UIColor? {
        return UIColor(red: 214/255, green: 231/255, blue: 242/255, alpha: 1.0)
    }
    
}
